/**
 * 获取指定等级的基础属性值
 * @param level 目标等级 (1-100)
 * @returns 该等级对应的基础属性对象
 */
export function getBaseStats(level: number): BaseStats {
  // 防御性处理，限制等级在 1-100 之间
  const safeLevel = Math.max(1, Math.min(100, Math.floor(level)));
  
  return {
    hp: BASE_STATS_TABLE.HP[safeLevel],
    mp: BASE_STATS_TABLE.MP[safeLevel],
    atk: BASE_STATS_TABLE.ATK[safeLevel],
    def: BASE_STATS_TABLE.DEF[safeLevel],
    matk: BASE_STATS_TABLE.MATK[safeLevel],
    mdef: BASE_STATS_TABLE.MDEF[safeLevel],
    agi: BASE_STATS_TABLE.AGI[safeLevel],
    luk: BASE_STATS_TABLE.LUK[safeLevel]
  };
}
